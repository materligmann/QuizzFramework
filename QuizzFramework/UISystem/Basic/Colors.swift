import UIKit

extension UIColor {
    @nonobjc class var primaryColor: UIColor {
        return .white
    }
    
    @nonobjc class var secondaryColor: UIColor {
        return .black
    }
    
    @nonobjc class var tertiaryColor: UIColor {
        return .lightGray
    }
    
    @nonobjc class var fourthColor: UIColor {
        return .systemBlue
    }
    
    @nonobjc class var fithColor: UIColor {
        return .systemGray6
    }
}

